#include <bits/stdc++.h>
using namespace ::std;
class Solution
{
public:
    vector<int> largestDivisibleSubset(vector<int> &nums)
    {
        sort(nums.begin(), nums.end());
        int n = nums.size();
        vector<int> dp(n, 1);
        vector<int> hash(n);
        int lastindex = 0;
        int maxx = 0;
        for (int i = 0; i < n; i++)
        {
            hash[i] = i;
            for (int j = 0; j < i; j++)
            {
                if (nums[i] % nums[j] == 0 && dp[i] < dp[j] + 1)
                {
                    dp[i] = dp[j] + 1;
                    hash[i] = j;
                }
            }
            if (maxx < dp[i])
            {
                maxx = dp[i];
                lastindex = i;
            }
        }
        vector<int> ans;
        ans.push_back(nums[lastindex]);
        while (hash[lastindex] != lastindex)
        {
            lastindex = hash[lastindex];
            ans.push_back(nums[lastindex]);
        }
        reverse(ans.begin(), ans.end());
        return ans;
    }
};
